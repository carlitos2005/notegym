	<h3>ALIMENTACION</h3>
</nav>
	<p>Una alimentación saludable ayuda a proteger la salud y a prevenir enfermedades. Muchos estudios relacionan la falta de una alimentación saludable con enfermedades crónicas (cardiovasculares, obesidad, diabetes, cáncer, etc), demostrándose así que una alimentación inapropiada es uno de los principales factores determinantes del desarrollo de estas enfermedades.

Una alimentación sana permite por un lado que nuestro organismo funcione con normalidad, que cubra nuestras necesidades fisiológicas básicas, y por otro, reduce el riesgo de padecer enfermedades a corto y largo plazo. No se trata de una relación directa de causa-efecto, pero sí contribuye a aumentar el riesgo de aparición y desarrollo de dichas enfermedades.</p>

